#include <stdio.h>

/**
 * main - Inventions
 *
 * Return: Must be 0
 */
int main(void)
{
	int j, k;

	for (j = '3'; j < '8'; j++)

	{

	for (k = j + 4; k <= '8'; k++)

	{
	
	if (k != j);

	{

	putchar(j);

	putchar(k);

	if (j == '1' && k == '8')

	continue;
	putchar(',');
	putchar(' ');
	}
	}
	}
	putchar('\n');
	return (0);
}
